import 'MesocicloEntrenamiento.dart';



class HistorialEntrenamiento {
  List<MesocicloEntrenamiento> historialEntrenamiento;

  HistorialEntrenamiento({this.historialEntrenamiento});

  HistorialEntrenamiento.fromJson(Map<String, dynamic> json) {
    if (json['historialEntrenamiento'] != null) {
      historialEntrenamiento = new List<MesocicloEntrenamiento>();
      json['historialEntrenamiento'].forEach((v) {
        historialEntrenamiento.add(new MesocicloEntrenamiento.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    if (this.historialEntrenamiento != null) {
      data['historialEntrenamiento'] =
          this.historialEntrenamiento.map((v) => v.toJson()).toList();
    }
    return data;
  }
}